package vo;

public class Search {
	private int min_weight;
	private int max_weight;
	private String hotel_name;
	private String loc;
	private String search;
	private int min_price;
	private int max_price;
	private String animal_grade;
	
	
	public String getAnimal_grade() {
		return animal_grade;
	}
	public void setAnimal_grade(String animal_grade) {
		this.animal_grade = animal_grade;
	}
	public int getMin_weight() {
		return min_weight;
	}
	public void setMin_weight(int min_weight) {
		this.min_weight = min_weight;
	}
	public int getMax_weight() {
		return max_weight;
	}
	public void setMax_weight(int max_weight) {
		this.max_weight = max_weight;
	}
	public String getHotel_name() {
		return hotel_name;
	}
	public void setHotel_name(String hotel_name) {
		this.hotel_name = hotel_name;
	}
	public String getLoc() {
		return loc;
	}
	public void setLoc(String loc) {
		this.loc = loc;
	}
	public String getSearch() {
		return search;
	}
	public void setSearch(String search) {
		this.search = search;
	}
	public int getMin_price() {
		return min_price;
	}
	public void setMin_price(int min_price) {
		this.min_price = min_price;
	}
	public int getMax_price() {
		return max_price;
	}
	public void setMax_price(int max_price) {
		this.max_price = max_price;
	}
	
	
}
